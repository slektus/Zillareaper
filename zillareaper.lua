local function FYLE_fake_script() -- Fake Script: StarterGui.ZGui.Main.Ultra Speed.LocalScript
    local script = Instance.new("LocalScript")
    script.Name = "LocalScript"
    script.Parent = Converted["_Ultra Speed"]
    local req = require
    local require = function(obj)
        local fake = fake_module_scripts[obj]
        if fake then
            return fake()
        end
        return req(obj)
    end

	print("Ultraspeed executed, .gg/zillakami")
	
	local runService = game:GetService("RunService")
	local userInputService = game:GetService("UserInputService")
	
	local character = game.Players.LocalPlayer.Character
	local hrp = character:WaitForChild("HumanoidRootPart")
	local camera = game.Workspace.CurrentCamera
	
	local bodyPos = Instance.new("BodyPosition", hrp)
	bodyPos.MaxForce = Vector3.new()
	bodyPos.D = 0
	
	local bodyGyro = Instance.new("BodyGyro", hrp)
	bodyGyro.MaxTorque = Vector3.new()
	bodyGyro.D = 10
	
	local speed = 1
	local isFlying = false
	
	local toggleKey = Enum.KeyCode.F
	
	local button = script.Parent
	
	function fly()
		isFlying = true
		bodyPos.MaxForce = Vector3.new(100000, 100000, 100000)
		bodyGyro.MaxTorque = Vector3.new(100000, 100000, 100000)
		runService:BindToRenderStep("fly", 1, function()
			bodyPos.Position = hrp.Position + ((hrp.Position - camera.CFrame.p).unit * speed)
			bodyGyro.CFrame = CFrame.new(camera.CFrame.p, hrp.Position)
		end)
	end
	
	function stopFlying()
		isFlying = false
		runService:UnbindFromRenderStep("fly")
		bodyPos.MaxForce = Vector3.new()
		bodyGyro.MaxTorque = Vector3.new()
	end
	
	button.Activated:Connect(function()
		if not isFlying then
			fly()
		else
			stopFlying()
		end
	end)
	
	userInputService.InputBegan:Connect(function(input, isProcessed)
		if not isProcessed and input.KeyCode == toggleKey then
			stopFlying()
		end
	end)
	
end
